// Esta fución llama a busqueda avanzada
function mostrarBusqueda(){
    document.getElementById('searchAd').style.display = 'block';
    document.getElementById('busqueda').style.display = 'none';
   }
 
     // Esta fución oculta la busqueda avanzada
     function ocultarBusquedaAvanzada(){
    document.getElementById('searchAd').style.display = 'none';
    document.getElementById('busqueda').style.display = 'block';
   }